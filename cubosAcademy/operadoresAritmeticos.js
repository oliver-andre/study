const x = 5;
const y = 6;

let resultado = x + y; //11
resultado = resultado + 2; //13
resultado += 2; //15 (operador soma)
resultado -= 2; //13 (operador subtração)
resultado *= 2; //26 (operador multiplicação)
resultado /= 2; //13 (operador divisão)

const resto = 15 % 5; //3 (operador de resto)

console.log(resultado);
console.log(resto);
